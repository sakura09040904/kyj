package lesson001;

public class E007_IfElseStmt {

	public static void main(String[] args) {
		// If... else... Statement
		// if(조건){ ...  } else { ... }
		int age = 17;
		
		// if else 구문의 예
		if(age >= 20 ) {
			System.out.println("성년... 출입 가능");
		}else {
			System.out.println("미성년... 출입 불가");
		}// end of if(age>20)
		
		System.out.println("프로그램 종료...");

	}// end of main()

}//end of class
